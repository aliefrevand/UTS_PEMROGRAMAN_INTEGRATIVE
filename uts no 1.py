# Mendefinisikan bilangan bulat
bilangan_bulat = 90

# Jumlah hari dalam setahun
jumlah_hari_dalam_setahun = 365

# Melakukan pembagian
hasil = bilangan_bulat / jumlah_hari_dalam_setahun

# Menampilkan hasil dengan tujuh desimal (dibulatkan ke atas)
print("Hasil pembagian:", round(hasil, 7))
